Cypress.Commands.add("credenciales", (user, password) => {
    cy.get(':nth-child(1) > :nth-child(1) > .ptur-inputContainer > .rb16m').click().type(user);
    cy.get('.inputNumeric').click().type(password)
    cy.xpath('//*[@id="root"]/ion-app/div/ion-router-outlet/div/ion-router-outlet/div/div/div[2]/div[4]/button').click()
})
